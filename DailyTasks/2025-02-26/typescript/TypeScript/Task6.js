// Example of type assertion
var x = "This is a string";
console.log("the string is: ".concat(x));
var strLength = (x).length;
console.log("The length of the string is: ".concat(strLength));
// parseInt()
var y = "123";
var convertedNumber = parseInt(y);
console.log("The converted number is: ".concat(convertedNumber));
// Number()
var z = "456";
var anotherConvertedNumber = Number(z);
console.log("The another converted number is: ".concat(anotherConvertedNumber));
// toString()
var num = 789;
var numAsString = num.toString();
console.log("The number as a string is: ".concat(numAsString));
